class Placing {
	constructor(id, code, name, type, insured, start, end, policy, brokerage, proposal, insurance) {
		this.id = id;
		this.code = code;
		this.name = name;
		this.type = type;
		this.insured = insured;
		this.start = start;
		this.end = end;
		this.policy = policy;
		this.brokerage = brokerage;
		this.proposal = proposal;
		this.insurance = insurance;
  }
}

export default Placing;